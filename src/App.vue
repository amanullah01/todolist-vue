<template>
  <div id="app">
    <Todos v-bind:todos="todos" v-on:del-todo="deleteTodo"/>
  </div>
</template>

<script>
import Todos from "./components/Todos";
export default {
  name: "app",
  components: {
    Todos
  },
  data() {
    return {
      todos: [
        {
          id: 1,
          title: "Todo One",
          completed: true
        },
        {
          id: 2,
          title: "Todo Two",
          completed: true
        },
        {
          id: 3,
          title: "Todo Three",
          completed: false
        },
        {
          id: 4,
          title: "Todo Four",
          completed: false
        }
      ]
    };
  },
  methods: {
    deleteTodo(id) {
      console.log(id);
      console.log(this.todos);
      this.todos = this.todos.filter(todo => todo.id !== id);
      console.log(this.todos);
    }
  }
};
</script>

